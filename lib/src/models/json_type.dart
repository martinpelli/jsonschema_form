/// This enum defines possible values for the type key in jsonSchema
enum JsonType {
  /// Represents a String in dart
  string,

  /// Represents a num in dart
  number,

  /// Represents an int in dart
  integer,

  /// Represents a bool in dart
  boolean,

  /// Represents a List in dart
  array,

  /// Represents a Map<String, dynamic> in dart
  object;
}
